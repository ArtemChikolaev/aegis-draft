export type OvrTier = "elite" | "strong" | "mid" | "low" | "weak" | "liability";

/** Тир игрока по OVR — для окраски и эффектов номера/карточки в драфте/итоге.
 *  Пороги калиброваны по реальному распределению pack-player OVR (7075 значений в
 *  packs.json: 54–99, медиана 74, p90 85, p95 87, p99 91).
 *
 *  Два края шкалы редкие и потому «событийные», остальное — рабочая середина:
 *    elite     ≥88  ~4.8%  — карточка-фольга (отражает свет)
 *    liability <60  ~1.9%  — погасшая карточка (свет не отражает)
 *  Середина (strong/mid/low/weak) различается только цветом номера и тонировкой фона:
 *  один weak покрывает ~29% игроков, эффект на нём был бы шумом, а не сигналом.
 *
 *  НЕ путать со scoreTier КОМАНДЫ (диапазон 80–96) — там свои пороги: смешивать домены
 *  нельзя, иначе типовой 74-игрок красится как «weak». */
export function playerOvrTier(ovr: number): OvrTier {
  if (ovr >= 88) return "elite";
  if (ovr >= 82) return "strong";
  if (ovr >= 76) return "mid";
  if (ovr >= 70) return "low";
  if (ovr >= 60) return "weak";
  return "liability";
}
